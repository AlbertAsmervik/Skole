public class ComparisonOperators {
    public static void main(String[]args) {
        // == is equal to
        // <= less than or equal to
        // < less than
        // > greater than
        // > greather then or equal to
        // != not equal to
        // || or
        // && and

        
        int a = 5;
        int b= 4;

        double c = 2.4;
        double d = 3.2;

        boolean e = (a != b);
        boolean f = (c == d);

        System.out.println(e);
        System.out.println(f);
    } 
}
